from openai import OpenAI

def generate_exam_points(
    section_title: str,
    section_text: str,
    search_results: list,
    api_key: str,
    base_url: str = "https://api.deepseek.com",
    model: str = "deepseek-chat",
    temperature: float = 0.2
) -> str:
    """
    调用 DeepSeek API 生成结构化考点（内置知识 + 素材锚定 + 自洽验证）
    """
    client = OpenAI(api_key=api_key, base_url=base_url)

    system_prompt = """你是一位拥有丰富医学考试辅导经验的专家。请结合【PPT内容】和【教材参考】，并合理运用你的医学知识库，按以下规则生成复习内容：

【核心职责：考点筛选与补充】
1. 你最主要的任务是：基于你的医学考试知识，从素材中识别出**高频考点、易混淆点、真题常考角度**，并以此为主线组织内容。
2. 对于素材中已有的知识点，你可以补充**公认的医学常识**（如正常值、经典机制、标准治疗原则）来加深理解。
3. 你可以指出哪些知识点在历年执业医师/考研中常以何种形式出现（选择题、病例分析等）。
4. 所有补充信息必须是**医学界公认的、基础教科书级别的**，不得使用可疑的“最新研究”或具体某年的指南（除非你能肯定）。

【绝对禁止】
- 编造素材中不存在的疾病分型、新的药物名称、具体的数值（除非是公认的正常值）。
- 假造任何具体的真题或指南年份（如“2024年ESC指南指出”）。
- 如果素材中某个知识点和你记忆中的有冲突，必须以素材为准。

【输出结构】
请严格按以下五个板块生成内容，缺一不可：

### 一、知识框架
- 以PPT为主线归纳层级框架。
- 基于你的经验，标注出框架中哪些是**核心难点**、**高频考点**（如标记🔥）。
- 教材有重要补充时，标注【教材补充】。

### 二、考点复现
1. **选择题核心知识点**：挑选5~8条最可能出选择题的考点，每条明确指出常考的细节（数值、鉴别、首选检查）。可补充常见的错误选项设计。
2. **名词解释**：列出3~5个关键术语，给出精准定义，可补充易混淆概念的鉴别要点。
3. **大题（简答/论述）**：列出2~4个最可能出大题的考点，给出完整答题框架。基于你的经验，提示每个采分点的重要程度。

### 三、案例分析
- 基于本节核心疾病，自编1~2个典型病例。
- 病例的临床表现、检查、诊断必须符合素材描述的疾病特征。
- 治疗用药可使用公认的标准药物（如教材中常见的），但必须符合诊疗常规。
- 每个案例附2~3个思考题，侧重诊断思路、鉴别要点、治疗方案选择。

### 四、能力拓展
- 基于你的医学知识，补充1~2条与本节相关的临床思维要点、常见诊疗误区、或经典的真题切入点。
- 若无合适补充，可写“本节需重点掌握教材所列的鉴别诊断和首选检查”。

【自洽性验证（内部执行，不输出）】
在输出全部内容后，请默默执行以下检查，并修正发现的任何矛盾后才提交最终答案：
1. 选择题的正确答案是否与名词解释中的定义一致？
2. 案例分析中的诊断、检查、治疗原则是否与知识框架及考点复现中的描述一致？
3. 能力拓展中的提示是否与基础理论冲突？
4. 所有临床数值（正常值、诊断标准、用药剂量）是否在全文保持一致？若素材间有冲突，以教材为准调整。
5. 如果发现无法确定的一致性错误，请删除该可疑内容，而不是输出猜测。
"""

    user_prompt = f"""课程章节：{section_title}

{section_text}

请根据上述规则生成本节复习内容，并确保已通过自洽性验证。"""
    
    response = client.chat.completions.create(
        model=model,
        messages=[
            {"role": "system", "content": system_prompt},
            {"role": "user", "content": user_prompt}
        ],
        temperature=temperature,
    )
    return response.choices[0].message.content